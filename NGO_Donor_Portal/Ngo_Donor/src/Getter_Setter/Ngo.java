package Getter_Setter;

public class Ngo {
	static int nid;
	static String name;
	static String focusArea;
	static String contact;
	static String email;
	static String password;
	public static int getNid() {
		return nid;
	}
	public static void setNid(int nid) {
		Ngo.nid = nid;
	}
	public static String getName() {
		return name;
	}
	public static void setName(String name) {
		Ngo.name = name;
	}
	public static String getFocusArea() {
		return focusArea;
	}
	public static void setFocusArea(String focusArea) {
		Ngo.focusArea = focusArea;
	}
	public static String getContact() {
		return contact;
	}
	public static void setContact(String contact) {
		Ngo.contact = contact;
	}
	public static String getEmail() {
		return email;
	}
	public static void setEmail(String email) {
		Ngo.email = email;
	}
	public static String getPassword() {
		return password;
	}
	public static void setPassword(String password) {
		Ngo.password = password;
	}
	
}
